<h2 id="literature_laundry_list">The Laundry List</h2>

<p>(Common survival traits of an Adult Child of Alcoholics and Dysfunctional Families)</p>
<ol class="list-normal">
	<li>We became isolated and afraid of people and authority figures.</li>
	<li>We became approval seekers and lost our identity in the process.</li>
	<li>We are frightened of angry people and any personal criticism.</li>
	<li>We either become alcoholics, marry them or both, or find another compulsive personality such as a workaholic to fulfill our sick abandonment needs.</li>
	<li>We live life from the viewpoint of victims and we are attracted by that weakness in our love and friendship relationships.</li>
	<li>We have an overdeveloped sense of responsibility and it is easier for us to be concerned with others rather than ourselves; this enables us not to look too closely at our own faults, etc.</li>
	<li>We get guilt feelings when we stand up for ourselves instead of giving in to others.</li>
	<li>We became addicted to excitement.</li>
	<li>We confuse love and pity and tend to "love" people we can "pity" and "rescue."</li>
	<li>We have "stuffed" our feelings from our traumatic childhoods and have lost the ability to feel or express our feelings because it hurts so much (Denial).</li>
	<li>We judge ourselves harshly and have a very low sense of self-esteem.</li>
	<li>We are dependent personalities who are terrified of abandonment and will do anything to hold on to a relationship in order not to experience painful abandonment feelings, which we received from living with sick people who were never there emotionally for us.</li>
	<li>Alcoholism is a family disease; and we became para-alcoholics and took on the characteristics of that disease even though we did not pick up the drink.</li>
	<li>Para-alcoholics are reactors rather than actors.</li> 
</ol>


<h2 id="literature_problem">The Problem</h2>

<p>Many of us found that we had several characteristics in common as a result of being
brought up in an alcoholic or other dysfunctional household. We had come to feel isolated, and uneasy with other people, especially authority figures. To protect ourselves, we became people-pleasers, even though we lost our own identities in the process. All the same we would mistake any personal criticism as a threat. We either became alcoholics (or practiced other addictive behavior) ourselves, or married them, or both. Failing that, we found other compulsive personalities, such as a workaholic, to fulfill our sick need for abandonment.
</p>

<p>We lived life from the standpoint of victims. Having an overdeveloped sense of responsibility, we preferred to be concerned with others rather than ourselves. We got guilt feelings when we stood up for ourselves rather than giving in to others. Thus, we became reactors rather than actors, letting others take the initiative. We were dependent personalities, terrified of abandonment, willing to do almost anything to hold on to a relationship in order not to be abandoned emotionally. Yet, we kept choosing insecure relationships because they matched our childhood relationship with alcoholic or dysfunctional parents.</p>

<p>These symptoms of the family disease of alcoholism or other dysfunction made us ‘co-victims’, those who take on the characteristics of the disease without necessarily ever taking a drink. We learned to keep our feelings down as children and kept them buried as adults. As a result of this conditioning, we confused love with pity, tending to love those we could rescue. Even more self-defeating, we became addicted to excitement in all our affairs, preferring constant upset to workable relationships. </p>
<p>This is a description, not an indictment.</p>

