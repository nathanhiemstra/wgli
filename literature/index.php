<h2 id="literature_solution">The Solution</h2>

<p><strong>The solution is to become your own loving parent</strong></p>

<p>As <acronym title="Adult Children of Alcoholics">ACA</acronym> becomes a safe place for you, you will find freedom to express
all the hurts and fears that you have kept inside and to free yourself
from the shame and blame that are carry-overs from the past. You will
become an adult who is imprisoned no longer by childhood reactions. You
will recover the child within you, learning to love and accept
yourself.</p>

<p>The healing begins when we risk moving out of isolation. Feelings and
buried memories will return. By gradually releasing the burden of
unexpressed grief, we slowly move out of the past. We learn to re-parent
ourselves with gentleness, humor, love and respect.</p>

<p>This process allows us to see our biological parents as the
instruments of our existence. Our actual parent is a Higher Power whom
some of us choose to call God. Although we had alcoholic or dysfunctional
parents, our Higher Power gave us the Twelve Steps of Recovery.</p>

<p>This is the action and work that heals us: we use the Steps; we use
the meetings; we use the telephone. We share our experience, strength,
and hope with each other. We learn to restructure our sick thinking one
day at a time. When we release our parents from responsibility for our
actions today, we become free to make healthful decisions as actors, not
reactors. We progress from hurting, to healing, to helping. We awaken to
a sense of wholeness we never knew was possible.</p>

<p>By attending these meetings on a regular basis, you will come to see
parental alcoholism or family dysfunction for what it is: a disease that
infected you as a child and continues to affect you as an adult. You will
learn to keep the focus on yourself in the here and now. You will take
responsibility for your own life and supply your own parenting.</p>

<p>You will not do this alone. Look around you and you will see others
who know how you feel. We love and encourage you no matter what. We ask
you accept us just as we accept you.</p>

<p>This is a spiritual program based on action coming from love. We are
sure that as the love grows inside you, you will see beautiful changes in
all your relationships, especially with your Higher Power, yourself, and
your parents.</p>

<h2 id="literature_promises">The 12 Promises</h2>

<ol class="list-normal">
	<li>We will discover our real identities by loving and acceptingourselves.</li>

	<li>Our self-esteem will increase as we give ourselves approval on a
	daily basis.</li>
	
	<li>Fear of authority figures and the need to "people-please" will leave us.</li>
	
	<li>Our ability to share intimacy will grow inside us.</li>
	
	<li>As we face our abandonment issues, we will be attracted by strengths and become more tolerant of weaknesses.</li>
	
	<li>We will enjoy feeling stable, peaceful, and financially secure.</li>
	
	<li>We will learn how to play and have fun in our lives.</li>
	
	<li>We will choose to love people who can love and be responsible for themselves.</li>
	
	<li>Healthy boundaries and limits will become easier for us to set.</li>
	
	<li>Fears of failures and success will leave us, as we intuitively make healthier choices.</li>
	
	<li>With help from our <acronym title="Adult Children of Alcoholics">ACA</acronym> support group, we will slowly release our dysfunctional behaviors.</li>
	
	<li>Gradually, with our Higher Power's help, we learn to expect the best and get it.</li>
</ol>


<h2 id="literature_steps">The 12 Steps</h2>

<ol class="list-normal">
	<li>We admitted we were powerless over the effects of alcoholism or other family dysfunction, that our lives had become unmanageable.</li>

	<li>Came to believe that a power greater than ourselves could restore us to sanity.</li>

	<li>Made a decision to turn our will and our lives over to the care of God as we understand God.</li>

	<li>Made a searching and fearless moral inventory of ourselves.</p></li>

	<li>Admitted to God, to our selves, and to another human being the exact nature of our wrongs.</li>

	<li>Were entirely ready to have God remove all these defects of character.</p></li>

	<li>Humbly asked God to remove our shortcomings.</li>

	<li>Made a list of all persons we had harmed and became willing to make amends to them all.</li>

	<li>Made direct amends to such people wherever possible, except when to do so would injure them or others.</li>

	<li>Continued to take personal inventory and, when we were wrong, promptly admitted it.</li>

	<li>Sought through prayer and meditation to improve our conscious contact with God, as we understand God, praying only for knowledge of God's will for us and the power to carry it out.</li>

	<li>Having had a spiritual awakening as a result of these steps, we tried to carry this message to others who still suffer, and to practice these principles in all our affairs.</li>
</ol>


<h2 id="literature_traditions">The 12 Traditions</h2>

<ol class="list-normal">
	<li>Our common welfare should come first, personal recovery depends&nbsp;on ACA unity.</li>

	<li>For our group purpose there is but one ultimate authority - a loving God as expressed in our group conscience. Our leaders are but trusted servants, they do not govern.</li>

	<li>The only requirement for membership in ACA is a desire to recover from the effects of growing up in an alcoholic or otherwise dysfunctional family.</li>

	<li>Each group is autonomous except in matters affecting other groups or ACA as a whole. We cooperate with all other 12-Step programs.</li>

	<li>Each group has but one primary purpose - to carry its  message to the adult child who still suffers.</li>

	<li>An ACA group ought never endorse, finance or lend the ACA name to any related facility or outside enterprise, lest problems of money, property and prestige divert us from our primary purpose.</li>

	<li>Every ACA group ought to be fully self-supporting, declining outside contributions.</li>

	<li>Adult Children of Alcoholics should remain forever non-professional, but our service centers may employ special workers.</li>

	<li>ACA, as such, ought never be organized, but we may create service boards or committees directly responsible to those they serve.</li>

	<li>Adult Children of Alcoholics has no opinion on outside issues; hence the ACA name ought never be drawn into public controversy.</li>

	<li>Our public relations policy is based on attraction rather than promotion; we maintain personal anonymity at the level of press, radio, T.V. and films.</li>

	<li>Anonymity is the spiritual foundation of our traditions, ever reminding us to place principles before personalities.</li>
</ol>

