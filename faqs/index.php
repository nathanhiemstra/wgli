<dl>
	<dt id="faq-aca-acoa">What’s the difference between <acronym title="Adult Children of Alcoholics">ACA</acronym> and ACoA?</dt>
	<dd>Both acronyms refer to Adult Children of Alcoholics.  When we were in our infancy, the meetings on the East Coast and Midwest used ACoA and those on the West Coast used <acronym title="Adult Children of Alcoholics">ACA</acronym>.  As the leadership of our organization gravitated to the West Coast, <acronym title="Adult Children of Alcoholics">ACA</acronym> became the more common acronym.  Since Adult Children groups under the auspices of Al-Anon are referred to as ACoA in some areas of the country, we prefer to call ourselves <acronym title="Adult Children of Alcoholics">ACA</acronym> to maintain this distinction.  
	</dd>
	<dt id="faq-alcoholic-home">Do I have to have come from an alcoholic home to come to meetings?</dt>
	<dd>No. Anyone who was raised in a dysfunctional family that was chaotic, unsafe, and/or lacked appropriate nurturing is welcome to attend our meetings, whether alcohol was a problem in the family or not.  Often our member’s had an alcoholic grandparent(s) who passed on the dysfunction.  Other members came from homes with dysfunctions such as an absent parent due to workaholism, ultra-religiousness, militarism, sexual abuse - whether overt or covert, eating disorders, and anything along these lines.  Unfortunately, there’s no end to the number of issues that can cause us to feel “less than.”  Many foster children, now adults, also relate to <acronym title="Adult Children of Alcoholics">ACA</acronym>.
	</dd>
	<dt id="faq-closed">What is a “closed” meeting?</dt>
	<dd>Most <acronym title="Adult Children of Alcoholics">ACA</acronym> meetings are closed. Closed meetings are not available to observers, for instance those in the education or psychology field doing research. Closed meetings are for any individuals who identify themselves as <acronym title="Adult Children of Alcoholics">ACA</acronym>s, or <a href="/is-aca-for-me/">those trying to decide if <acronym title="Adult Children of Alcoholics">ACA</acronym> is for them</a>. 
	</dd>
	<dt id="faq-religious">Is <acronym title="Adult Children of Alcoholics">ACA</acronym> a religious organization?  When I read the 12 Steps, there are a lot of references to God.</dt>
	<dd><acronym title="Adult Children of Alcoholics">ACA</acronym> is a spiritual, not a religious program.  We refer to a Higher Power, which is a self-defined concept for each member.  The word “God” is meant to be generic for any belief.  Each member’s beliefs are a personal matter, and we do not discuss recovery in terms of any specific faith. Specific religious views should not be promoted.   
	</dd>
	<dt id="faq-higher-power">How do you define “Higher Power”?</dt>
	<dd>Higher Power is something greater than ourselves that aids us in our recovery.  It can be a spiritual being or not - it’s whatever you decide it is.  
	</dd>

	<dt id="agnostics">Is <acronym title="Adult Children of Alcoholics">ACA</acronym> available to agnostics?</dt> 
	<dd>Yes. Agnostics are certainly welcome.</dd> 


	<dt id="church-affiliation">A lot of meetings are held at churches.  Are they affiliated with those churches?</dt> 
	<dd>No, we are not affiliated with any churches. Meetings are often held in churches because they offer very reasonable rent.</dd> 

	<dt id="faq-cost">What does it cost to attend an <acronym title="Adult Children of Alcoholics">ACA</acronym> meeting?</dt>
	<dd>Our meetings pass the basket and ask for voluntary contributions for operating expenses.  Generally meetings suggest a $2 donation. However, this donation is not a requirement - it is a freewill offering. There are no dues or membership fees.
	</dd>

	<dt id="faq-not-available">What if there isn’t a meeting that’s available to me?</dt>
	<dd>You might consider starting your own meeting.  That’s what many of us have done.  The <a href="http://www.adultchildren.org/lit/Handbook.s"><acronym title="Adult Children of Alcoholics">ACA</acronym> Fellowship Text</a>, more commonly referred to as the <a href="http://www.adultchildren.org/lit/Handbook.s">Big Red Book</a>, provides information that will help you.  This book, as well as a <a href="http://www.adultchildren.org/lit/Literature.s" >Sample Meeting Packet</a>, are available on the <acronym title="Adult Children of Alcoholics">ACA</acronym> World Service website:  <a href="http://www.adultchildren.org/">www.AdultChildren.org</a>.

	There are also telephone and internet meetings available.
	</dd>	


	<dt id="teenagers">Are teenagers allowed to attend <acronym title="Adult Children of Alcoholics">ACA</acronym> meetings?</dt>
	<dd>Any adult teenager (18 or older) is welcome at our meetings. For liability reasons, it may be best for minors to find more age appropriate support groups. We suggest minors consult a guidance counselor or therapist for direction.</dd>

	<dt id="new-members">Is the group accepting new members at this time?</dt>
	<dd>New members are always welcome.</dd>

	<dt id="age-gender">What is the age range and gender distribution of people at meetings?</dt>
	<dd>Meetings can have a wide distribution of ages and generally have both men and women. However, each meeting is unique.  <a href="/meetings/">Contact individual meetings</a> for more info.</dd>

	<dt id="politics">Is the group accepting of all viewpoints or does it generally run liberal or conservative?</dt>
	<dd>Meetings have no opinion on outside issues, including politics. While individual members may be liberal or conservative, topics discussed focus on recovery only.</dd>


	<dt id="average-attendance">How many people come each week on average?</dt>  
	<dd>Generally, meetings have six to 20 regular members.  Attendance usually fluctuates.</dd>  

	<dt id="alcoholism-cured">Can alcoholism be cured?</dt>  
	<dd> Some people would like to think that there are ways to cure alcoholism.  But most experts who know all about alcoholics will tell you that, unfortunately, there is no magic pill. <br><br>  
	There are many paths for alcoholics to find sobriety, from going to a treatment program, which usually directs the alcoholic to Alcoholics Anonymous meetings for maintenance, to toughing it out on their own. However, the result of the <strong>“alcoholism self-treatment plan”</strong> is often either a sober <strong>alcoholic bully</strong>, meaning someone who thinks and acts the same, but just doesn’t drink, or someone who <strong>develops another form of addiction</strong>, such as workaholism, extreme religious views, or any number of other compulsive behaviors.</dd>  

	<dt id="aa-alanon">Is <acronym title="Adult Children of Alcoholics">ACA</acronym> part of A.A. or Al-Anon?</dt>  
	<dd>No. We are an independent organization and are not allied with any other 12 Step program. However, our 12 Steps and 12 Traditions are adapted from Alcoholics Anonymous, and we cooperate with all other 12 Step programs.  </dd>  

	<dt id="who-runs-aca">Who runs <acronym title="Adult Children of Alcoholics">ACA</acronym>?</dt>  
	<dd><acronym title="Adult Children of Alcoholics">ACA</acronym> is run by its members.<br><br>
		<ul class="list-normal list-normal-size">
			<li><strong>Globally</strong><br>
			<acronym title="Adult Children of Alcoholics">ACA</acronym> has a <a href="http://www.adultchildren.org/" target="_blank">World Service Organization</a> (WSO) that helps the fellowship remain unified by providing  information and producing literature.  Individual meetings are free to operate as they wish, as long as they follow the Steps and Traditions.  </li>
			<li><strong>Regionally</strong><br>
			Some regional areas form Intergroups, which are service organizations composed of representatives from individual meetings to unify and serve meetings of that region.
			</li>
			<li class="list-single-space"><strong>Locally</strong><br>
			Each meeting has individuals who fill responsibilities that help keep the meeting running safely and smoothly, such as chairperson, treasurer, secretary, etc.  These positions rotate.  
			</li>
		</ul>
	</dd>  



	<dt id="therapy">Is <acronym title="Adult Children of Alcoholics">ACA</acronym> therapy?</dt>  
	<dd>No, although it can be very therapeutic to attend.  Therapy and the <acronym title="Adult Children of Alcoholics">ACA</acronym> program can work hand-in-hand and neither one should be considered a substitute for the other. Many members are referred to <acronym title="Adult Children of Alcoholics">ACA</acronym> by therapists and social workers to help them work through childhood issues.  </dd>  

	<dt id="identify-myself-or-talk">Do I have to identify myself or say anything in an <acronym title="Adult Children of Alcoholics">ACA</acronym> meeting?</dt>  
	<dd>We identify ourselves by first names only to preserve our anonymity.  Whether you say anything further about yourself is up to you. If you attend a meeting where they go around the room taking turns sharing, and you do not want to share, just say ‘pass’. Otherwise you can just listen.</dd>

	<dt id="what-talk-about">What do people talk about in <acronym title="Adult Children of Alcoholics">ACA</acronym> meetings?</dt>  
	<dd>We talk about what it was like growing up in a dysfunctional home, how it has affected us as adults, and how we’re learning to live more fulfilling lives. For many, these meetings are the first time we have talked about these topics in a setting where others understand and don’t judge us. We can finally break the rules of our dysfunctional homes of: “don’t talk, don’t trust, don’t feel”. More about what meetings are like.</dd>

