<h1>“Childhood Messages”</h1>



<h3>What it was like</h3>
<p>I grew up in a constant state of fear of my alcoholic stepfather&rsquo;s unpredictable, explosive anger. I felt like a cinder block was five feet above my head at all times and could fall on me at any second, for any reason.</p>
<p>Messages I came to believe as a kid:
</p>
<ul class="list-normal">
  <li><strong>I&rsquo;m not safe and learning something new is terrifying.<br />
  </strong>I didn&rsquo;t know how to swim. Every time we went swimming, before I could play on my own, he would hold my head under water and count. He would let me up if I couldn&rsquo;t hold out, but I had to do it again and again until I met the number he designated. The number increased with each trip. It felt like all of Lake Michigan was trying to push its way into my eight-year-old lungs. </li>

  <li><strong>My best is not enough.<br />
  </strong>When his car was stuck in the snow he yelled after me to come and dig him out. He got more and more angry as I shoveled and shoveled while the tires spun so fast and loud against the pavement and wet snow a couple feet from my face. I can still smell the burning rubber. </li>

  <li><strong>My body and self-respect are not important.<br />
    </strong>Once I threw away a piece of old hardened cheese. He made me get it out of the trash and eat it. I could taste the coffee grinds on it. Another time, after he made me eat a bowl of oatmeal (which I hated), I threw up. He forced me to eat another bowl.<br />
  </li>
  <li><strong>It&rsquo;s not safe to relax.<br />
  </strong>Every day I would start to feel anxiety knowing he was about to come home. Every sound of that routine is still vivid to me: his slowing car up the hill; out of gear; turning into the driveway; gravel under the tires; car stops; engine off; emergency brake up; door open; door shut; eight seconds of silence; work boots up the steps; screen door opens; main door opens; screen door shuts; main door shuts; keys, thermos, and lunch box on the table. He walks into the living room and I find out what kind of night I&rsquo;m going to have. </li>

  <li><strong>No one can protect me, I have to take abuse.<br />
  </strong>My mom stayed in the relationship until I was seventeen. Her attempts to stand up to him were usually ineffective. Standing up for myself was <em>always</em> ineffective. </li>
</ul>
<p>I thought the abuse would stop if I would just behave the way he wanted or somehow prove I was a good person. It didn&rsquo;t.</p>
<h3>What Happened</h3>
<p>These messages became part of my self-image. How could they not? I spent 6000 days living like this, in the most formative years of my life, with the people that I was wired to trust and model my behavior after?</p>
<p>My number one life goal has always been: <strong><em>do not become like my step dad</em></strong>. I put tremendous pressure on myself to monitor every thought, feeling, and voice inflexion - to never be angry, unfair, selfish, or unempathetic. When I failed, I felt anxiety and panic that I was becoming a monster.</p>
<p>As a kid I tried to be unnoticeable. I learned to not have feelings, wants, never need from people, and be self-sufficient. I spent hours outside or in my room by myself. When I was left home alone it was heaven! I could finally relax and be myself.</p>
<p>In school I felt a sense of belonging that I was missing at home, especially with sports. But when I didn&rsquo;t have a role to play, I often felt like an outsider and played class clown to feel comfortable.</p>
<p>In college it was hard to make new friends. I felt that if I didn&rsquo;t have fascinating or funny things to say, why would someone like me? I isolated by going to movies and taking long drives.</p>
<p>Romantic relationships were difficult. During arguments I shut down and became unaware of my feelings and thoughts. Eventually I would go numb and leave the relationship.</p>
<p><strong>Hitting a bottom.</strong></p>
<p>At thirty, I quit my job to be self-employed and work from home. It felt safe with no authority figure (like being home alone as a kid). I felt powerful and safe to be self sufficient, but I became a slave to my work. I was so afraid of not doing good enough work that I would agonize over decisions, spending extra days and weeks on projects. I coped with this stress by eating, going to movies, excessive internet, video games– things that put my work further behind. The coping mechanisms that worked in my teens and twenties stopped working.</p>
<p><strong>The results:</strong></p>
<ul class="list-normal list-single-space">
  <li>Constantly broke from getting little work done and not asking to be paid what I was worth</li>
  <li>Staying with clients who mistreated me and didn&rsquo;t pay</li>
  <li>Ignoring emails and bills</li>
  <li>Procrastinated paying taxes for six years resulting in $40,000 of debt</li>
  <li>Consistently full voicemail</li>
  <li>Jumping every time the phone ring or I a door closed</li>
  <li>My car always falling apart</li>
  <li>100 lbs. overweight and pulling my back twice</li>
  <li>Ashamed of my appearance</li>
  <li>Procrastinated seeing the doctor for twenty years and dentist for ten years and lost a tooth</li>
  <li>Eight years without a romantic relationship</li>
  <li>Bottling up my feelings because I didn&rsquo;t believe my friends wanted to hear my problems </li>
  <li>Didn&rsquo;t see friends much (unless they needed my help)</li>
  <li>Was distracted by unfinished freelance work when I was around friends and family </li>
  <li>Very lonely (a feeling only now can I actually recognize)</li>
  <li>Depressed and didn&rsquo;t care about living</li>
</ul>
<p>By my mid-thirties, I knew I was stuck and unhappy and desperately wanted to change. I was looking for something else but didn&rsquo;t know what. Spirituality? Anything. I just knew this wasn&rsquo;t the extraordinary life I always wanted and expected for myself.</p>
<p>A friend, who was in ACA, showed me the <a href="/literature/#literature_laundry_list">14 ACA traits</a> in the <a href="http://www.adultchildren.org/lit/Handbook.s" target="_blank">Red Book</a>. I had every single trait (except, I thought, &nbsp;&ldquo;fear of abandonment&rdquo;, because I believed I didn&rsquo;t need anybody.&nbsp;It turns out that&rsquo;s my biggest trait). I felt a tremendous sense of relief and affirmation when I saw in print all issues I had always struggled with. I went to an ACA meeting with my friend and finally found where I belonged.</p>
<p>I started going to meetings every week.</p>
<h3>What it&rsquo;s like in recovery</h3>
<p>At first I was out the door as soon as the meeting was over. Eventually I started developing relationships. Now I have a support network. When something upsets me (an interaction at work, fear of a potential problem, etc.) I call an ACA friend. Without fail, the problem becomes more manageable. This still difficult because I can feel shame for needing someone and for even having the problem, but it&rsquo;s becoming normal. </p>
<p>I started learning to manage problems that before just seemed part of life. Example:</p>
<p>I wanted to be not so constantly afraid of people. I was afraid to ask questions of my boss or to knock on a public bathroom door. I was paralyzed, imagining people yelling at me for not &ldquo;knowing better.&rdquo; </p>
<p>Before recovery, I would avoid, isolate, eat, and keep my fears inside where they stayed big and scary. </p>
<p>In recovery, I learned I was expecting others to behave like my step dad and was terrified of re-experiencing traumatic childhood situations. I&rsquo;ve learned to shine a light on my fears through talking or journaling, making them manageable. I talk to my inner child with encouragement to replace the old abusive messages with new healthy ones. The result is many situations no longer faze me. When they do, I manage better and I bounce back quicker. </p>
<p><strong>After four years in ACA: </strong></p>
<ul class="list-normal list-single-space">
  <li>I have a job where I&rsquo;m around actual people</li>
  <li>My job treats and pays me well</li>
  <li>I exercise, eat well, and have lost 90 lbs</li>
  <li>I&rsquo;m paying off my taxes</li>
  <li>I feel financially secure</li>
  <li>I have insurance and see a doctor regularly</li>
  <li>I got my teeth fixed</li>
  <li>I&rsquo;m much more comfortable around people and am better at being myself rather than saying what I think they will like</li>
  <li>I state my opinion even, if it&rsquo;s contrary to others (including a boss)</li>
  <li>Instead of trying to &ldquo;fix&rdquo; friends and family, I work at loving, supporting, and accepting them as they are</li>
  <li>I see a therapist</li>
  <li>I have a reliable car</li>
  <li>I&rsquo;ve been dating</li>
  <li>I don&rsquo;t date or spend time with people who mistreat me</li>
  <li>I have better boundaries</li>
  <li>I say no when something isn&rsquo;t good for me</li>
  <li>I don&rsquo;t feel as much that I have to provide something for someone to like or love me</li>
  <li>I answer the phone and emails</li>
  <li>I&rsquo;m reliable, present, and more emotionally available</li>
  <li>I&rsquo;m no longer afraid I will turn into my step dad</li>
  <li>I feel more like myself.</li>
</ul>
<p>
  I still have lots of growing to do, but progress is steady. I&rsquo;m so grateful to have found ACA. 
</p>

