<p><strong>What is Adult Children of Alcoholics (<acronym title="Adult Children of Alcoholics">ACA</acronym> or <acronym title="Adult Children of Alcoholics">ACoA</acronym>)?</strong></p>

<p>ACA is an anonymous <a href="/literature/#literature_steps">Twelve Step</a>, Twelve Tradition program (adapted from the AA program) of women and men who grew up in an alcoholic or otherwise dysfunctional homes. We <a href="/meetings/">meet</a> with each other in a mutually respectful, safe environment and acknowledge our common experiences. We discover how childhood affected us in the past and influences us in the present (see <a href="/literature/#literature_laundry_list">The Laundry List</a>, and <a href="/literature/#literature_problem">The Problem</a>). We take positive action. By practicing the <a href="/literature/#literature_steps">Twelve Steps</a>, focusing on <a href="/literature/#literature_solution">The Solution</a>, and accepting a loving Higher Power of our understanding, we find freedom from the past and a way to improve our lives today. <a href="http://www.adultchildren.org/">&raquo; Read more</a></p>


